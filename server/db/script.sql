-- Restaurant table
CREATE TABLE restaurant (
    id INT PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    store_img VARCHAR(1024),
    image VARCHAR(1024),
    ratings DECIMAL(2,1)
);

-- Food table
CREATE TABLE food (
    foodID INT PRIMARY KEY,
    foodName VARCHAR(255) NOT NULL,
    price INT NOT NULL,
    foodImage VARCHAR(1024),
    restaurant_id INT,
    FOREIGN KEY (restaurant_id) REFERENCES restaurant(id)
);

-- Food category table
CREATE TABLE category (
    keyword VARCHAR(100) PRIMARY KEY
);

-- Food category mapping table
CREATE TABLE food_category (
    foodID INT,
    keyword VARCHAR(100),
    PRIMARY KEY (foodID,keyword),
    FOREIGN KEY (foodID) REFERENCES food(foodID),
    FOREIGN KEY (keyword) REFERENCES category(keyword)
);

